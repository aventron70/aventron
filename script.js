const storageKey = 'aventron-language';
const productStorageKey = 'aventron-product-selection';

let activeLanguage = 'fr';
let testimonialIndex = 0;
let activeProduct = { size: '150', variant: 'N' };

function getTranslation(language, key) {
  return key.split('.').reduce((value, segment) => value?.[segment], translations[language]);
}

function formatTemplate(template, values) {
  return Object.entries(values).reduce((output, [key, value]) => {
    return output.replace(`{${key}}`, value);
  }, template);
}

function getCollectorKey(size) {
  if (size === '200') {
    return 'collector200';
  }

  if (size === '300') {
    return 'collector300';
  }

  return 'collector150';
}

function updateProductConfigurator() {
  if (!productModel || !productSummary || !productBadge || !productCapacity || !productCollector || !productControl) {
    return;
  }

  const languageCatalog = productCatalog[activeLanguage];
  const code = `AT${activeProduct.size}${activeProduct.variant}`;
  const collector = languageCatalog[getCollectorKey(activeProduct.size)];
  const isSmart = activeProduct.variant === 'S';
  const variantLabel = isSmart ? languageCatalog.smartLabel : languageCatalog.normalLabel;
  const summaryTemplate = isSmart ? languageCatalog.summarySmart : languageCatalog.summaryNormal;

  productModel.textContent = code;
  productSummary.textContent = formatTemplate(summaryTemplate, {
    capacity: `${activeProduct.size}L`,
    collector,
  });
  productBadge.textContent = `${activeProduct.size}L / ${variantLabel}`;
  productCapacity.textContent = `${activeProduct.size}L`;
  productCollector.textContent = collector;
  productControl.textContent = isSmart ? languageCatalog.smartControl : languageCatalog.normalControl;

  productSizeButtons.forEach((button) => {
    button.classList.toggle('is-active', button.dataset.productSize === activeProduct.size);
  });

  productVariantButtons.forEach((button) => {
    button.classList.toggle('is-active', button.dataset.productVariant === activeProduct.variant);
  });

  window.localStorage.setItem(productStorageKey, JSON.stringify(activeProduct));
}

function applyLanguage(language) {
  const nextLanguage = translations[language] ? language : 'fr';
  const dictionary = translations[nextLanguage];

  activeLanguage = nextLanguage;
  document.documentElement.lang = nextLanguage;
  document.documentElement.dir = dictionary.dir;

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    const translatedValue = getTranslation(nextLanguage, key);

    if (typeof translatedValue !== 'string') {
      return;
    }

    const attributeName = node.dataset.i18nAttr;

    if (attributeName) {
      node.setAttribute(attributeName, translatedValue);
      return;
    }

    node.innerHTML = translatedValue;
  });

  languageButtons.forEach((button) => {
    button.classList.toggle('is-active', button.dataset.langSwitch === nextLanguage);
  });

  window.localStorage.setItem(storageKey, nextLanguage);
  updateProductConfigurator();
}

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
      }
    });
  },
  { threshold: 0.16 }
);

revealItems.forEach((item) => {
  if (!item.classList.contains('is-visible')) {
    revealObserver.observe(item);
  }
});

function setTestimonial(index) {
  if (!testimonialCards.length) {
    return;
  }

  testimonialIndex = (index + testimonialCards.length) % testimonialCards.length;

  testimonialCards.forEach((card, cardIndex) => {
    card.classList.toggle('is-active', cardIndex === testimonialIndex);
  });

  testimonialDots.forEach((dot, dotIndex) => {
    dot.classList.toggle('is-active', dotIndex === testimonialIndex);
  });
}

languageButtons.forEach((button) => {
  button.addEventListener('click', () => {
    applyLanguage(button.dataset.langSwitch || 'fr');
  });
});

productSizeButtons.forEach((button) => {
  button.addEventListener('click', () => {
    activeProduct = { ...activeProduct, size: button.dataset.productSize || '150' };
    updateProductConfigurator();
  });
});

productVariantButtons.forEach((button) => {
  button.addEventListener('click', () => {
    activeProduct = { ...activeProduct, variant: button.dataset.productVariant || 'N' };
    updateProductConfigurator();
  });
});

if (prevButton && nextButton && testimonialCards.length) {
  prevButton.addEventListener('click', () => setTestimonial(testimonialIndex - 1));
  nextButton.addEventListener('click', () => setTestimonial(testimonialIndex + 1));

  testimonialDots.forEach((dot, index) => {
    dot.addEventListener('click', () => setTestimonial(index));
  });

  window.setInterval(() => {
    setTestimonial(testimonialIndex + 1);
  }, 5000);
}

if (contactForm && contactSuccess) {
  contactForm.addEventListener('submit', (event) => {
    event.preventDefault();
    contactSuccess.textContent = translations[activeLanguage].contact.form.success;
    contactForm.reset();
  });
}

if (heroBackground) {
  window.addEventListener('scroll', () => {
    const offset = Math.min(window.scrollY * 0.12, 48);
    heroBackground.style.transform = `scale(1.08) translateY(${offset}px)`;
  });
}

try {
  const storedProduct = JSON.parse(window.localStorage.getItem(productStorageKey) || '{}');

  if (['150', '200', '300'].includes(storedProduct.size) && ['N', 'S'].includes(storedProduct.variant)) {
    activeProduct = storedProduct;
  }
} catch (error) {
  activeProduct = { size: '150', variant: 'N' };
}

applyLanguage(window.localStorage.getItem(storageKey) || 'fr');
